    public void mostrar(JTable tabla) {

        Conexion c = new Conexion();

        DefaultTableModel modelo = new DefaultTableModel();

        TableRowSorter<TableModel> OrdenarTabla = new TableRowSorter<TableModel>(modelo);
        tabla.setRowSorter(OrdenarTabla);

        String sql = "";

        modelo.addColumn("DNI");
        modelo.addColumn("Nombre");
        modelo.addColumn("Apellido");
        modelo.addColumn("FechaNac");
        modelo.addColumn("Clave");
        tabla.setModel(modelo);

        sql = "select DNI, Nombre, Apellido, FechaDeNac, contrase√±a from persona;";

        String[] datos = new String[5];
        Statement st;

        try {

            st = c.getConexion().createStatement();

            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {

                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);

                modelo.addRow(datos);
            }

            tabla.setModel(modelo);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No se pudo mostrar los registros: " + e.toString());
        }

    }
    
  

